// 1. Relational / Comparison Operators

// > - More than
// < - Less than
// >= - More than or equal
// <= Less than or equal

var result = 5 > 2
console.log(result)

result = 5 < 2
console.log(result)

result = 5 >= 5
console.log(result)

result = 5 <= 2
console.log(result)

// Equalit Operators
